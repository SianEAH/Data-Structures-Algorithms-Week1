    private void save(){
        File f;
        FileOutputStream fos;
        ObjectOutputStream oos;
        try{
            f = new File("contactList.txt");
            fos = new FileOutputStream(f);
            oos = new ObjectOutputStream(fos);
            
            oos.writeObject(contacts); //writing the array list to the file
            oos.close(); //close it
            System.out.println("Saved to file");
       }catch(IOException e){
            System.out.println("Exception saving to file "+e);
        }
    }
    
    //load method
     private void load(){
        File f;
        FileInputStream fis;
        ObjectInputStream ois;
        try{
            f = new File("contactList.txt");
            fis = new FileInputStream(f);
            ois = new ObjectInputStream(fis);
            
            contacts = (ArrayList<AddressBook>)ois.readObject(); //cast it to User obj in ArrayList
            ois.close();
            displayTA.append("");
        }catch(IOException | ClassNotFoundException e){
            System.out.println("Loading file error " +e);
        }
    }
    
