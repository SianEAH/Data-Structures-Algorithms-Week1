    private void addBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBTNActionPerformed
        // TODO add your handling code here:
         fName = fNameTF.getText();
        sName = sNameTF.getText();
        email = emailTF.getText();
        phone = phoneTF.getText();
        address = addressTF.getText();
        
        boolean exists = false;
        
        // Check for empty fields
        if (fName.isEmpty() || sName.isEmpty() || phone.isEmpty()) {
            JOptionPane.showMessageDialog(null,"Please enter a first name, a last name and a phone number");
            return;
        }

    for (AddressBook con : contacts) {
        if (con.getfName().equals(fName)
            && con.getsName().equals(sName)
            && con.getEmail().equals(email)
            && con.getPhone().equals(phone)
            && con.getAddress().equals(address)) {
            exists = true;
            break;
        }
    }

    if (exists) {
    JOptionPane.showMessageDialog(null, "That contact already exists in your phone book");
    } else {
    AddressBook tempS = new AddressBook(fName,sName,email,phone,address);
    contacts.add(tempS);
    JOptionPane.showMessageDialog(null, "Contact added!");
    }
    
    save();
    
    fNameTF.setText("");
    sNameTF.setText("");
    emailTF.setText("");
    phoneTF.setText("");
    addressTF.setText("");

    }//GEN-LAST:event_addBTNActionPerformed

    private void displayBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayBTNActionPerformed
        // TODO add your handling code here:
        fName = fNameTF.getText();
        sName = sNameTF.getText();
        email = emailTF.getText();
        phone = phoneTF.getText();
        address = addressTF.getText();
        
        //Set the text to what is in the ArrayList
        StringBuffer sBuff = new StringBuffer();
        for (AddressBook con: contacts) {
            sBuff.append(con.toString());
            sBuff.append("\n");
        }
        
        String allContacts = sBuff.toString();
        displayTA.setText(allContacts);
    }//GEN-LAST:event_displayBTNActionPerformed

    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
        fName = fNameTF.getText();
        sName = sNameTF.getText();
        email = emailTF.getText();
        phone = phoneTF.getText();
        address = addressTF.getText();
        
        //Make sure at least one field is filled in
        if(phone.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a phone number");
        }
        
        //If Statement
        for (AddressBook con : contacts) {
            if (con.getPhone().equals(phone)) {
                displayTA.setText(con.toString());
                return;
            }
        }

        displayTA.setText("No matching contact found");
        
        fNameTF.setText("");
        sNameTF.setText("");
        emailTF.setText("");
        phoneTF.setText("");
        addressTF.setText("");

    }//GEN-LAST:event_searchBTNActionPerformed

    private void deleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTNActionPerformed
        // TODO add your handling code here:
        fName = fNameTF.getText();
        sName = sNameTF.getText();
        email = emailTF.getText();
        phone = phoneTF.getText().trim();
        address = addressTF.getText();
        
        //Make sure every field is filled in
        if(phone.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter a phone number to delete");
            return;
        }
        
        //If Statement
        AddressBook toDelete = null;

        for (AddressBook con : contacts) {
            if (con.getPhone().equals(phone)) {
                toDelete = con;
                break;
            }
        }

        if (toDelete != null) {
            contacts.remove(toDelete);
            JOptionPane.showMessageDialog(null, "Contact deleted!");
        } else {
            JOptionPane.showMessageDialog(null, "No match found");
        }
        
        save();
        
        fNameTF.setText("");
        sNameTF.setText("");
        emailTF.setText("");
        phoneTF.setText("");
        addressTF.setText("");

    }//GEN-LAST:event_deleteBTNActionPerformed

